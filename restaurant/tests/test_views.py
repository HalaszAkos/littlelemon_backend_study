from django.test import TestCase
from restaurant.models import Menu
from restaurant.serializers import MenuSerializer


class MenuViewTest(TestCase):
    def setUp(self):
        # Add test instances
        Menu.objects.create(Title="Pizza", Price=12.99, Inventory=50)
        Menu.objects.create(Title="Burger", Price=8.99, Inventory=30)
        Menu.objects.create(Title="Pasta", Price=10.99, Inventory=40)

    def test_getall(self):
        # Retrieve all Menu objects
        items = Menu.objects.all()
        serializer = MenuSerializer(items, many=True)
        
        # Make a GET request to the menu endpoint
        response = self.client.get('/restaurant/menu/')
        
        # Assert status code
        self.assertEqual(response.status_code, 200)
        
        # Assert serialized data equals response data
        self.assertEqual(response.data, serializer.data)
